#ifndef CARDVALUE_H_INCLUDED
#define CARDVALUE_H_INCLUDED

/**
 * An enum class representing all standard card values.
 */
enum class CardValue
{
    /**
     * Card value of ace.
     */
    ACE,

    /**
     * Card value of two.
     */
    TWO,

    /**
     * Card value of three.
     */
    THREE,

    /**
     * Card value of four.
     */
    FOUR,

    /**
     * Card value of five.
     */
    FIVE,

    /**
     * Card value of six.
     */
    SIX,

    /**
     * Card value of seven.
     */
    SEVEN,

    /**
     * Card value of eight.
     */
    EIGHT,

    /**
     * Card value of nine.
     */
    NINE,

    /**
     * Card value of ten.
     */
    TEN,

    /**
     * Card value of jack.
     */
    JACK,

    /**
     * Card value of queen.
     */
    QUEEN,

    /**
     * Card value of king.
     */
    KING
};

#endif // CARDVALUE_H_INCLUDED
