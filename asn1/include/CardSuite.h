#ifndef SUITE_H_INCLUDED
#define SUITE_H_INCLUDED

/**
 * An enum class representing all standard card suites.
 */
enum class CardSuite
{
    /**
     * Clubs suite.
     */
    CLUB,

    /**
     * Spades suite.
     */
    SPADE,

    /**
     * Diamonds suite.
     */
     DIAMOND,

     /**
      * Hearts suite.
      */
     HEART
};

#endif // SUITE_H_INCLUDED
